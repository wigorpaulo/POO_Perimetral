package model;

public class TipoPessoa {

  // Atributos

  private int id;
  private String tipo;
  private String nome;
  
    // Metodos
  
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
		
	
 }
}
